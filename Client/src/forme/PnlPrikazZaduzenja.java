    private void jbtnIzmeniZaduzenjeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnIzmeniZaduzenjeActionPerformed
        int red = jtblPrikazZaduzenja.getSelectedRow();
        if (red == -1) {
            JOptionPane.showMessageDialog(this, "Niste odabrali zaduženje!", "Sistemska poruka", JOptionPane.WARNING_MESSAGE);
        }
        ZaduzenjeTableModel model = (ZaduzenjeTableModel) jtblPrikazZaduzenja.getModel();
        Zaduzenje z = model.vratiZaduzenje(red);
        if (z.getDatumRazduzenja() != null && z.isVraceno() && z.getArhivatorRazduzio().getArhivatorID() != 1000) {
            JOptionPane.showMessageDialog(this, "Nije dozvoljeno menjati razdužena zaduženja!", "Sistemska poruka", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Util.getInstance().putZaduzenje("izabrano_zaduzenje", z);

        PnlUnosZaduzenja panel = new PnlUnosZaduzenja(k);
        panel.setPanel(this);
        JDialog d1 = (JDialog) SwingUtilities.getWindowAncestor(this);
        JFrame f = (JFrame) SwingUtilities.getWindowAncestor(d1);
        JDialog d = new JDialog(f, "Rad sa korisnikom: " + k.getNaziv(), true);
        d.setLayout(new BorderLayout());
        d.add(panel, BorderLayout.CENTER);
        d.pack();
        d.setLocationRelativeTo(this);
        d.setVisible(true);
    }//GEN-LAST:event_jbtnIzmeniZaduzenjeActionPerformed

