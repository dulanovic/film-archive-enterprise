    private void srediFormu() {
        jlblNazivKorisnika.setText(k.getNaziv());
        ZaduzenjeTableModel model = new ZaduzenjeTableModel(k.getListaZaduzenja());
        jtblPrikazZaduzenja.setModel(model);

        TableColumn tc0 = jtblPrikazZaduzenja.getColumnModel().getColumn(0);
        tc0.setPreferredWidth(65);
        TableColumn tc1 = jtblPrikazZaduzenja.getColumnModel().getColumn(1);
        tc1.setPreferredWidth(65);
        TableColumn tc2 = jtblPrikazZaduzenja.getColumnModel().getColumn(2);
        tc2.setPreferredWidth(70);
        TableColumn tc3 = jtblPrikazZaduzenja.getColumnModel().getColumn(3);
        tc3.setPreferredWidth(40);
        TableColumn tc4 = jtblPrikazZaduzenja.getColumnModel().getColumn(4);
        tc4.setPreferredWidth(140);
    }


